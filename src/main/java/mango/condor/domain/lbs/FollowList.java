public class FollowList {
	private long id;
	private String follower; // 关注者
	private String followed; // 被关注者
	private long followTime;		// 关注时间
	private boolean followedClearMsg;	// 被关注者是否清理掉该消息
	private boolean readMsg;		// 是否已读该消息

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getFollower() {
		return follower;
	}

	public void setFollower(String follower) {
		this.follower = follower;
	}

	public String getFollowed() {
		return followed;
	}

	public void setFollowed(String followed) {
		this.followed = followed;
	}

	public long getFollowTime() {
		return followTime;
	}

	public void setFollowTime(long followTime) {
		this.followTime = followTime;
	}

	public boolean isFollowedClearMsg() {
		return followedClearMsg;
	}

	public void setFollowedClearMsg(boolean followedClearMsg) {
		this.followedClearMsg = followedClearMsg;
	}

	public boolean isReadMsg() {
		return readMsg;
	}

	public void setReadMsg(boolean readMsg) {
		this.readMsg = readMsg;
	}

}
