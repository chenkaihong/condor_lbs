public class ThrowBottleReqMsg extends LBSRequestMessage {
	
	private String text;
	private String vid;
	
	public ThrowBottleReqMsg() {
		super(LBSMessageDefine.BOTTLE_THROW_REQUEST);
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getVid() {
		return vid;
	}

	public void setVid(String vid) {
		this.vid = vid;
	}
	
}
