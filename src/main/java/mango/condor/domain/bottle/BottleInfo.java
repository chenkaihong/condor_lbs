package mango.condor.domain.bottle;

public class BottleInfo {
	private long bottleId;			// 瓶子的id
	
	private int serverId;			// 对方的 服务器id
	private String serverName;		// 对方的 服务器名称
	private int playerId;			// 对方的playerId
	private String name;			// 对方的名字
	private String imageId;			// 对方的头像
	private String sign;			// 对方的签名
	
	private boolean isFollow;		// 是否关注了对方
	
	private boolean beSender;		// 是否扔瓶子的人
	private String msg;				// 瓶子的内容 - 文字
	private String vid;				// 瓶子的内容 - 语音
	private String replyText;		// 瓶子的 回复内容 (语音or文字)
	
	public long getBottleId() {
		return bottleId;
	}
	public void setBottleId(long bottleId) {
		this.bottleId = bottleId;
	}
	public int getServerId() {
		return serverId;
	}
	public void setServerId(int serverId) {
		this.serverId = serverId;
	}
	public String getServerName() {
		return serverName;
	}
	public void setServerName(String serverName) {
		this.serverName = serverName;
	}
	public int getPlayerId() {
		return playerId;
	}
	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImageId() {
		return imageId;
	}
	public void setImageId(String imageId) {
		this.imageId = imageId;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public boolean isFollow() {
		return isFollow;
	}
	public void setFollow(boolean isFollow) {
		this.isFollow = isFollow;
	}
	public boolean isBeSender() {
		return beSender;
	}
	public void setBeSender(boolean beSender) {
		this.beSender = beSender;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	public String getReplyText() {
		return replyText;
	}
	public void setReplyText(String replyText) {
		this.replyText = replyText;
	}
}